//! `robust` - does TRAINING-TIME AUGMENTATION improve HONEST cross-dataset
//! generalization of the learned drone detectors?
//!
//! ## The problem this attacks
//! Our learned detectors hit ~1.0 ROC-AUC *in-distribution* on DADS but the
//! companion `xeval` binary showed that, when you FIT on DADS and TEST on a
//! disjoint corpus (Al-Emadi drones as positives, ESC-50 confusable classes as
//! negatives), the learned templates COLLAPSE toward chance (~0.49–0.70). The
//! physics methods (`envelope_periodicity`, `hps`, `fusion`) held up far better
//! (~0.85–0.87). That gap is the signature of leakage / overfit: the learned
//! models memorize recording-level idiosyncrasies of DADS instead of the drone
//! signature itself.
//!
//! **The honest caveat carried over from `xeval`:** DADS is a MERGE SUPERSET
//! that already *contains* Al-Emadi and ESC-50. So even the "cross-dataset"
//! numbers here are optimistic - the test positives/negatives may share a
//! recording lineage with training clips. We do not claim these are truly
//! held-out; the bonus section below attempts a genuinely external positive
//! source and documents the outcome. Read every number with that asterisk.
//!
//! ## What this binary does
//! For the strong learned detectors (`mfcc_lr`, `feature_fusion`; `gtcc_lr` and
//! `mfcc_mlp` included for breadth) we train TWICE and compare cross-dataset:
//!
//! * **(a) plain** - fit on the raw DADS train split (the `xeval` baseline).
//! * **(b) augmented** - fit on the DADS train split *expanded* with realistic,
//!   deterministic, seeded perturbations (below). Same model, same eval; only
//!   the training data differs.
//!
//! The thesis being tested: augmentation should NARROW the generalization gap -
//! lift cross-dataset ROC-AUC / calibrated-F1 toward the in-distribution number
//! - without wrecking the in-distribution score. We quantify the lift per
//! detector and report it honestly, including when augmentation *fails* to help.
//!
//! ## The augmentation pipeline (deterministic, seeded)
//! Each original DADS train clip spawns `--variants` augmented copies. Every
//! variant draws a deterministic sequence of perturbations from a per-clip
//! seeded xorshift RNG (seed = global seed mixed with the clip index and variant
//! index), so the whole expansion is reproducible bit-for-bit:
//!
//! 1. **random gain** - scale by a factor in `[0.5, 1.5]`.
//! 2. **time shift / crop** - circular roll by a random offset, then (1/3 of the
//!    time) crop to a random 60–100% window and zero-pad back, simulating
//!    onset/segmentation jitter.
//! 3. **additive noise at a random SNR** - white OR pink noise (coin flip) added
//!    at an SNR drawn in `--snr-lo..--snr-hi` dB. Pink noise is generated by a
//!    Voss-McCartney octave generator (deterministic), better matching the 1/f
//!    spectra of real environmental backgrounds than white noise.
//! 4. **hard-negative background mixing** - with probability `--bg-mix-p`, a real
//!    ESC-50 *background* clip (from the augmentation fold pool, see leakage note)
//!    is mixed UNDER the DADS clip at a random background-SNR, teaching the model
//!    that a drone heard over wind / engine / chatter is still a drone.
//!
//! Additionally we synthesize **extra hard NEGATIVES** for the augmented training
//! set: ESC-50 background clips alone (label 0), optionally with a faint
//! out-of-band hum, so the classifier sees non-drone mechanical/broadband sounds
//! at train time rather than meeting them for the first time at test.
//!
//! ## Leakage discipline on the ESC-50 backgrounds (important)
//! The cross-dataset TEST negatives are ESC-50 clips. If we also drew
//! augmentation backgrounds from ESC-50 we could leak the exact test clips into
//! training and fake a generalization gain. To prevent that, ESC-50 is split by
//! its native 5-fold column: **folds 1–3 → augmentation background pool**,
//! **folds 4–5 → cross-dataset test negatives**. The two pools are disjoint by
//! construction, and we further use *different class sets* (engine/airplane/etc.
//! for backgrounds is allowed, but the clip files never overlap). This keeps the
//! "augmentation helped" claim honest.
//!
//! ## Resampling
//! ESC-50 is 44.1 kHz; DADS and Al-Emadi are 16 kHz. We reuse the exact
//! anti-aliased rational resampler documented in `xeval` (box low-pass of width
//! `floor(src/dst)` + linear interpolation) so the two binaries are comparable.

