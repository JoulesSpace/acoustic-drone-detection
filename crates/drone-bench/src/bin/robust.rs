// ----------------------------------------------------------------------------
// Deterministic RNG (xorshift32) - local copy; the harness one is private.
// ----------------------------------------------------------------------------

struct Rng(u32);
impl Rng {
    fn new(seed: u32) -> Self {
        Rng(seed.max(1))
    }
    fn next_u32(&mut self) -> u32 {
        let mut x = self.0;
        x ^= x << 13;
        x ^= x >> 17;
        x ^= x << 5;
        self.0 = x;
        x
    }
    /// Uniform in `[0, 1)`.
    fn unit01(&mut self) -> f32 {
        self.next_u32() as f32 / u32::MAX as f32
    }
    /// Uniform in `[-1, 1)`.
    fn bipolar(&mut self) -> f32 {
        self.unit01() * 2.0 - 1.0
    }
    /// Uniform in `[lo, hi)`.
    fn range(&mut self, lo: f32, hi: f32) -> f32 {
        lo + (hi - lo) * self.unit01()
    }
    fn chance(&mut self, p: f32) -> bool {
        self.unit01() < p
    }
}

/// Mix three integers into a stable seed so per-clip augmentation is independent
/// yet fully reproducible.
fn mix_seed(global: u32, a: usize, b: usize) -> u32 {
    let mut h = global
        .wrapping_mul(0x9E37_79B9)
        .wrapping_add(a as u32)
        .wrapping_mul(0x85EB_CA6B)
        .wrapping_add(b as u32)
        .wrapping_mul(0xC2B2_AE35);
    h ^= h >> 15;
    h.max(1)
}

// ----------------------------------------------------------------------------
// Resampling (identical to `xeval` for comparability; see its module docs).
// ----------------------------------------------------------------------------

fn resample(x: &[f32], src_sr: u32, dst_sr: u32) -> Vec<f32> {
    if src_sr == dst_sr || x.is_empty() {
        return x.to_vec();
    }
    let filtered: Vec<f32> = if src_sr > dst_sr {
        let width = (src_sr / dst_sr).max(1) as usize;
        if width <= 1 {
            x.to_vec()
        } else {
            box_lowpass(x, width)
        }
    } else {
        x.to_vec()
    };
    let ratio = src_sr as f64 / dst_sr as f64;
    let out_len = ((x.len() as f64) / ratio).floor() as usize;
    let mut out = Vec::with_capacity(out_len);
    for i in 0..out_len {
        let pos = i as f64 * ratio;
        let i0 = pos.floor() as usize;
        let frac = (pos - i0 as f64) as f32;
        let a = filtered[i0.min(filtered.len() - 1)];
        let b = filtered[(i0 + 1).min(filtered.len() - 1)];
        out.push(a + (b - a) * frac);
    }
    out
}

fn box_lowpass(x: &[f32], width: usize) -> Vec<f32> {
    let w = width.max(1);
    let half = w / 2;
    let mut out = vec![0.0_f32; x.len()];
    for (i, o) in out.iter_mut().enumerate() {
        let lo = i.saturating_sub(half);
        let hi = (i + half).min(x.len() - 1);
        let mut acc = 0.0_f32;
        for &v in &x[lo..=hi] {
            acc += v;
        }
        *o = acc / (hi - lo + 1) as f32;
    }
    out
}

// ----------------------------------------------------------------------------
// Signal helpers used by the augmentation pipeline.
// ----------------------------------------------------------------------------

/// Mean signal power (energy per sample).
fn power(x: &[f32]) -> f32 {
    if x.is_empty() {
        return 0.0;
    }
    x.iter().map(|&v| v * v).sum::<f32>() / x.len() as f32
}

/// Scale `noise` so that, added to `signal`, it lands at `snr_db`, then return
/// the gain to apply to the noise. Guards against silent signals/noise.
fn noise_gain_for_snr(signal: &[f32], noise: &[f32], snr_db: f32) -> f32 {
    let ps = power(signal).max(1e-12);
    let pn = power(noise).max(1e-12);
    // target_pn = ps / 10^(snr/10)  ->  gain = sqrt(target_pn / pn)
    let target_pn = ps / 10f32.powf(snr_db / 10.0);
    (target_pn / pn).sqrt()
}

/// White noise of length `n`, drawn from the given RNG.
fn white_noise(n: usize, rng: &mut Rng) -> Vec<f32> {
    (0..n).map(|_| rng.bipolar()).collect()
}

/// Pink (1/f) noise via the Voss-McCartney octave algorithm (deterministic).
fn pink_noise(n: usize, rng: &mut Rng) -> Vec<f32> {
    const ROWS: usize = 16;
    let mut rows = [0.0_f32; ROWS];
    for r in rows.iter_mut() {
        *r = rng.bipolar();
    }
    let mut out = Vec::with_capacity(n);
    let mut max_abs = 1e-9_f32;
    for i in 0..n {
        // Each row updates when its corresponding bit of the counter flips.
        let counter = i as u32;
        for (r, row) in rows.iter_mut().enumerate() {
            if r < 31 && (counter & (1 << r)) != 0 && ((counter.wrapping_sub(1)) & (1 << r)) == 0 {
                *row = rng.bipolar();
            }
        }
        let v: f32 = rows.iter().sum::<f32>() / ROWS as f32;
        max_abs = max_abs.max(v.abs());
        out.push(v);
    }
    // Normalize to roughly unit peak so SNR scaling is meaningful.
    for v in out.iter_mut() {
        *v /= max_abs;
    }
    out
}

/// Circularly roll `x` left by `shift` samples (deterministic time shift).
fn circular_shift(x: &[f32], shift: usize) -> Vec<f32> {
    if x.is_empty() {
        return Vec::new();
    }
    let s = shift % x.len();
    let mut out = Vec::with_capacity(x.len());
    out.extend_from_slice(&x[s..]);
    out.extend_from_slice(&x[..s]);
    out
}

/// Crop `x` to a window of `frac` of its length starting at `start_frac`, then
/// zero-pad back to the original length (simulates segmentation jitter).
fn crop_pad(x: &[f32], start_frac: f32, frac: f32) -> Vec<f32> {
    let n = x.len();
    if n == 0 {
        return Vec::new();
    }
    let win = ((n as f32) * frac).round().clamp(1.0, n as f32) as usize;
    let start = (((n - win) as f32) * start_frac).round() as usize;
    let mut out = vec![0.0_f32; n];
    out[start..start + win].copy_from_slice(&x[start..start + win]);
    out
}

/// Add `b` (already gain-applied) into `a` in place, length-matched.
fn add_in_place(a: &mut [f32], b: &[f32], gain: f32) {
    let m = a.len().min(b.len());
    for i in 0..m {
        a[i] += b[i] * gain;
    }
}

/// Peak-normalize to `peak` if the clip clips, else leave alone. Keeps audio in
/// `[-1, 1]` after additive mixing without changing quiet clips.
fn safe_normalize(x: &mut [f32], peak: f32) {
    let m = x.iter().fold(0.0_f32, |acc, &v| acc.max(v.abs()));
    if m > peak && m > 0.0 {
        let g = peak / m;
        for v in x.iter_mut() {
            *v *= g;
        }
    }
}

