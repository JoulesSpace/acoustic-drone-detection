fn instantiate(name: &str) -> Box<dyn Approach> {
    approaches::all()
        .into_iter()
        .find(|a| a.name() == name)
        .unwrap_or_else(|| panic!("unknown approach {name}"))
}

fn score_samples(approach: &dyn Approach, samples: &[Sample]) -> Vec<(f32, u8)> {
    samples
        .iter()
        .map(|s| {
            (
                approach.score(&s.samples, s.sample_rate).clamp(0.0, 1.0),
                s.label,
            )
        })
        .collect()
}

