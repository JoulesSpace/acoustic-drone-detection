/// One ESC-50 clip plus its class (for the per-class breakdown if needed).
struct EscClip {
    sample: Sample,
    #[allow(dead_code)]
    category: String,
}

/// Load ESC-50 clips for `classes` whose fold is in `folds`, resampled to 16 kHz.
/// `per_class` caps clips per class. Deterministic by filename sort.
fn load_esc50(
    root: &Path,
    classes: &[&str],
    folds: &[u32],
    per_class: usize,
    label: u8,
) -> Result<Vec<EscClip>, Box<dyn Error>> {
    let meta = root.join("meta").join("esc50.csv");
    let text =
        std::fs::read_to_string(&meta).map_err(|e| format!("reading {}: {e}", meta.display()))?;

    // (category -> sorted filenames) restricted to wanted classes + folds.
    let mut by_class: BTreeMap<String, Vec<String>> = BTreeMap::new();
    for (i, line) in text.lines().enumerate() {
        if i == 0 {
            continue; // header: filename,fold,target,category,esc10,src_file,take
        }
        let cols: Vec<&str> = line.split(',').collect();
        if cols.len() < 4 {
            continue;
        }
        let filename = cols[0].trim();
        let fold: u32 = cols[1].trim().parse().unwrap_or(0);
        let category = cols[3].trim();
        if classes.contains(&category) && folds.contains(&fold) {
            by_class
                .entry(category.to_string())
                .or_default()
                .push(filename.to_string());
        }
    }

    let audio_dir = root.join("audio");
    let mut out = Vec::new();
    for (category, mut files) in by_class {
        files.sort();
        for f in files
            .into_iter()
            .take(if per_class > 0 { per_class } else { usize::MAX })
        {
            let p = audio_dir.join(&f);
            let (audio, sr) = read_mono_wav(&p)?;
            let audio = resample(&audio, sr, TARGET_SR);
            out.push(EscClip {
                sample: Sample {
                    id: format!("esc50/{category}/{f}"),
                    samples: audio,
                    sample_rate: TARGET_SR,
                    label,
                },
                category: category.clone(),
            });
        }
    }
    Ok(out)
}

/// Load Al-Emadi drone clips as positives (deterministic stride subsample).
fn load_alemadi_positives(root: &Path, max: usize) -> Result<Vec<Sample>, Box<dyn Error>> {
    let dir = root.join("Binary_Drone_Audio").join("yes_drone");
    let mut files: Vec<PathBuf> = std::fs::read_dir(&dir)
        .map_err(|e| format!("reading {}: {e}", dir.display()))?
        .filter_map(|e| e.ok().map(|e| e.path()))
        .filter(|p| p.extension().map(|x| x == "wav").unwrap_or(false))
        .collect();
    files.sort();
    let stride = if max > 0 && files.len() > max {
        files.len().div_ceil(max)
    } else {
        1
    };
    let mut out = Vec::new();
    for p in files
        .iter()
        .step_by(stride)
        .take(if max > 0 { max } else { usize::MAX })
    {
        let (audio, sr) = read_mono_wav(p)?;
        let audio = resample(&audio, sr, TARGET_SR);
        out.push(Sample {
            id: format!("alemadi/{}", p.file_name().unwrap().to_string_lossy()),
            samples: audio,
            sample_rate: TARGET_SR,
            label: 1,
        });
    }
    Ok(out)
}

// ----------------------------------------------------------------------------
// The augmentation pipeline.
// ----------------------------------------------------------------------------

struct AugConfig {
    variants: usize,
    snr_lo: f32,
    snr_hi: f32,
    bg_mix_p: f32,
    seed: u32,
}

/// Produce one augmented variant of `clip` using the per-variant `rng` and the
/// pool of background clips `bgs`. Returns audio at the same sample rate.
fn augment_one(clip: &Sample, bgs: &[Sample], cfg: &AugConfig, rng: &mut Rng) -> Vec<f32> {
    let mut x = clip.samples.clone();
    if x.is_empty() {
        return x;
    }

    // 1) Random gain.
    let gain = rng.range(0.5, 1.5);
    for v in x.iter_mut() {
        *v *= gain;
    }

    // 2) Time shift, then occasionally crop+pad (segmentation jitter).
    let shift = (rng.unit01() * x.len() as f32) as usize;
    x = circular_shift(&x, shift);
    if rng.chance(1.0 / 3.0) {
        let frac = rng.range(0.6, 1.0);
        let start = rng.unit01();
        x = crop_pad(&x, start, frac);
    }

    // 3) Additive white/pink noise at a random SNR.
    let snr = rng.range(cfg.snr_lo, cfg.snr_hi);
    let noise = if rng.chance(0.5) {
        white_noise(x.len(), rng)
    } else {
        pink_noise(x.len(), rng)
    };
    let g = noise_gain_for_snr(&x, &noise, snr);
    add_in_place(&mut x, &noise, g);

    // 4) Hard-negative background mixing (real ESC-50 clip under the drone).
    if !bgs.is_empty() && rng.chance(cfg.bg_mix_p) {
        let bg = &bgs[(rng.next_u32() as usize) % bgs.len()];
        // Tile/trim the background to the clip length, with a random start.
        let bg_clip = tile_to_len(&bg.samples, x.len(), rng.next_u32() as usize);
        let bg_snr = rng.range(cfg.snr_lo, cfg.snr_hi);
        let bg_gain = noise_gain_for_snr(&x, &bg_clip, bg_snr);
        add_in_place(&mut x, &bg_clip, bg_gain);
    }

    safe_normalize(&mut x, 0.99);
    x
}

/// Tile (loop) or trim `src` to exactly `len` samples, starting at `start`.
fn tile_to_len(src: &[f32], len: usize, start: usize) -> Vec<f32> {
    if src.is_empty() {
        return vec![0.0; len];
    }
    let s = start % src.len();
    (0..len).map(|i| src[(s + i) % src.len()]).collect()
}

/// Build the augmented training set: every original clip is kept, plus
/// `cfg.variants` perturbed copies, plus a set of extra hard NEGATIVES built
/// from ESC-50 background clips (so the model meets non-drone mechanical sounds
/// at train time). Fully deterministic.
fn build_augmented(train: &[Sample], bgs: &[Sample], cfg: &AugConfig) -> Vec<Sample> {
    let mut out: Vec<Sample> = Vec::with_capacity(train.len() * (cfg.variants + 1) + bgs.len());

    // Keep originals, then add variants.
    for (ci, clip) in train.iter().enumerate() {
        out.push(clip.clone());
        for vi in 0..cfg.variants {
            let mut rng = Rng::new(mix_seed(cfg.seed, ci, vi + 1));
            let aug = augment_one(clip, bgs, cfg, &mut rng);
            out.push(Sample {
                id: format!("{}#aug{vi}", clip.id),
                samples: aug,
                sample_rate: clip.sample_rate,
                label: clip.label,
            });
        }
    }

    // Extra hard negatives: ESC-50 backgrounds alone (label 0). These are the
    // augmentation-fold clips, disjoint from the test negatives. We add a faint
    // out-of-band hum to half of them as an additional non-drone confounder.
    for (bi, bg) in bgs.iter().enumerate() {
        let mut rng = Rng::new(mix_seed(cfg.seed.wrapping_add(777), bi, 1));
        let mut x = bg.samples.clone();
        // Resample/normalize already done at load. Light gain jitter + noise.
        let gain = rng.range(0.7, 1.2);
        for v in x.iter_mut() {
            *v *= gain;
        }
        if rng.chance(0.5) {
            // faint low hum, deliberately NOT a harmonic stack (non-drone).
            let f = rng.range(45.0, 75.0);
            let sr = bg.sample_rate as f32;
            for (i, v) in x.iter_mut().enumerate() {
                let t = i as f32 / sr;
                *v += 0.05 * (2.0 * std::f32::consts::PI * f * t).sin();
            }
        }
        let snr = rng.range(cfg.snr_lo, cfg.snr_hi);
        let noise = white_noise(x.len(), &mut rng);
        let g = noise_gain_for_snr(&x, &noise, snr);
        add_in_place(&mut x, &noise, g);
        safe_normalize(&mut x, 0.99);
        out.push(Sample {
            id: format!("{}#negaug", bg.id),
            samples: x,
            sample_rate: bg.sample_rate,
            label: 0,
        });
    }

    out
}

// ----------------------------------------------------------------------------
// Eval plumbing.
